def sync_gap_analysis_ticket_column(registry: dict[str, Any] | None = None) -> None:
    test_gap_analysis_round_trip()
    registry = registry or load_registry()
    path = gap_analysis_path()
    original = path.read_text(encoding="utf-8")
    doc = parse_gap_analysis_tables(original)
    for table in doc.tables:
        for row in table.rows:
            if len(row) >= 5:
                row[3] = _lookup_ticket_for_gap(registry, row[0], row[1], row[4])
    updated = write_gap_analysis_tables(doc, ticket_column=True)
    path.write_text(updated, encoding="utf-8")


def inject_marker_block(path: Path, start: str, end: str, inner: str) -> None:
    text = path.read_text(encoding="utf-8")
    if start not in text or end not in text:
        raise RuntimeError(f"Missing markers in {path}: {start} / {end}")
    before, rest = text.split(start, 1)
    _, after = rest.split(end, 1)
    new_text = before + start + "\n" + inner.rstrip() + "\n" + end + after
    path.write_text(new_text, encoding="utf-8")


def inject_status_block(registry: dict[str, Any] | None = None) -> None:
    registry = registry or load_registry()
    tickets = registry.get("tickets", [])
    shipped = [t for t in tickets if t.get("status") == "shipped"]
    shipped.sort(key=lambda t: t.get("order", 9999), reverse=True)
    latest = shipped[0]["id"] if shipped else "T-066"
    slice_row = next((t for t in tickets if t.get("active_slice")), None)
    lines = [f"**Latest shipped:** **{latest}**", ""]
    if slice_row:
        lines.append(
            f"**ACTIVE NOW:** **{slice_row['id']}** — {slice_row.get('active_slice', '')} "
            f"({slice_row.get('title', '')}). Spec: `{slice_row.get('spec', '')}`."
        )
    else:
        ready = sorted(
            [t for t in tickets if t.get("status") == "ready"],
            key=lambda t: t.get("order", 9999),
        )
        if ready:
            r = ready[0]
            lines.append(f"**ACTIVE NOW:** **{r['id']}** — {r.get('title', '')}")
    lines.append("")
    lines.append("**Next (by order):**")
    queued = [
        t for t in tickets if t.get("status") in ("queued", "ready") and t.get("order")
    ]
    queued.sort(key=lambda t: t.get("order", 9999))
    for t in queued[:10]:
        lines.append(f"- **{t['id']}** — {t.get('title', '')} (`{t.get('status')}`)")
    inject_marker_block(
        repo_root() / "CLAUDE.md", STATUS_MARKER_START, STATUS_MARKER_END, "\n".join(lines)
    )


def inject_next_block(registry: dict[str, Any] | None = None) -> None:
    registry = registry or load_registry()
    lines = ["### Recommended next work (auto-generated)", ""]
    open_t = [
        t
        for t in registry.get("tickets", [])
        if t.get("status") in ("ready", "queued", "running", "review")
        and t.get("order")
    ]
    open_t.sort(key=lambda t: (t.get("order", 9999), t.get("id", "")))
    for t in open_t[:10]:
        lines.append(f"- **{t['id']}** — {t.get('title', '')} ({t.get('status')})")
    inject_marker_block(
        repo_root() / "Design_Docs/Mission_Creator_Architecture/ROADMAP.md",
        NEXT_MARKER_START,
        NEXT_MARKER_END,
        "\n".join(lines),
    )


def _ticket_sort_key(t: dict[str, Any]) -> tuple[int, str]:
    return (t.get("order") or 99999, t.get("id", ""))


def generate_ticket_registry_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    rows = sorted(registry.get("tickets", []), key=_ticket_sort_key)
    lines = [
        AUTO_HEADER,
        "# Ticket Registry",
        "",
        "| T-ID | Order | Status | Program | Title | Summary |",
        "|------|-------|--------|---------|-------|---------|",
    ]
    for t in rows:
        summary = (t.get("summary") or "").replace("|", "\\|")
        lines.append(
            f"| {t['id']} | {t.get('order', '—')} | {t['status']} | {t.get('program', '')} | "
            f"{t.get('title', '')} | {summary} |"
        )
    lines.append("")
    return "\n".join(lines)


def generate_ticket_lead_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    tickets = registry.get("tickets", [])
    lines = [AUTO_HEADER, "# Ticket Lead Dashboard", ""]
    for label, statuses in (
        ("Running / Review", ("running", "review")),
        ("Ready", ("ready",)),
        ("Next queued (top 10)", ("queued",)),
    ):
        subset = [t for t in tickets if t.get("status") in statuses]
        subset.sort(key=_ticket_sort_key)
        if label.startswith("Next"):
            subset = subset[:10]
        lines.append(f"## {label}")
        lines.append("")
        for t in subset:
            lines.append(
                f"- **{t['id']}** ({t.get('order', '—')}) — {t.get('title', '')} "
                f"[{t.get('status')}] — {t.get('summary', '')}"
            )
        lines.append("")
    lines.append("## Dependency graph (scoped)")
    lines.append("")
    lines.append("```mermaid")
    lines.append("flowchart LR")
    graph_ids: set[str] = set()
    for t in tickets:
        if t.get("status") in ("running", "review", "ready"):
            graph_ids.add(t["id"])
    queued = sorted(
        [t for t in tickets if t.get("status") == "queued" and t.get("order")],
        key=_ticket_sort_key,
    )[:5]
    for t in queued:
        graph_ids.add(t["id"])
    for t in tickets:
        if t.get("status") == "shipped":
            for other in tickets:
                if other["id"] in graph_ids and t["id"] in (other.get("depends_on") or []):
                    graph_ids.add(t["id"])
    for t in tickets:
        if t["id"] not in graph_ids:
            continue
        for dep in t.get("depends_on") or []:
            if dep in graph_ids:
                dep_n = dep.replace("-", "")
                tid_n = t["id"].replace("-", "")
                lines.append(f"  {dep_n}[{dep}] --> {tid_n}[{t['id']}]")
    lines.append("```")
    lines.append("")
    return "\n".join(lines)


def generate_ticket_dev_queue_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    ready = [t for t in registry.get("tickets", []) if t.get("status") == "ready"]
    ready.sort(key=_ticket_sort_key)
    lines = [AUTO_HEADER, "# Developer Queue", "", "Only `ready` tickets with specs.", ""]
    for t in ready:
        tid = t["id"]
        branch = t.get("branch", f"ticket/{tid}")
        lines.append(f"## {tid} — {t.get('title', '')}")
        lines.append("")
        lines.append(f"- **Spec:** `{t.get('spec', '')}`")
        lines.append(f"- **Branch:** `{branch}`")
        lines.append(f"- **Summary:** {t.get('summary', '')}")
        lines.append("")
    return "\n".join(lines)


def generate_ticket_brainstorm_md(registry: dict[str, Any] | None = None) -> str:
    registry = registry or load_registry()
    lines = [AUTO_HEADER, "# Ticket Brainstorm", "", "`idea` + `deferred` only.", ""]
    by_program: dict[str, list[dict[str, Any]]] = {}
    for t in registry.get("tickets", []):
        if t.get("status") not in ("idea", "deferred"):
            continue
        by_program.setdefault(t.get("program", "platform"), []).append(t)
    for program in sorted(by_program):
        lines.append(f"## {program}")
        lines.append("")
        for t in sorted(by_program[program], key=_ticket_sort_key):
            surfaces = ", ".join(t.get("surfaces") or [])
            lines.append(
                f"- **{t['id']}** ({t.get('status')}) — {t.get('title', '')} "
                f"[{surfaces}] — {t.get('summary', '')}"
            )
        lines.append("")
    return "\n".join(lines)


def generate_queue_json(registry: dict[str, Any] | None = None) -> dict[str, Any]:
    registry = registry or load_registry()
    pipeline = [
        t
        for t in registry.get("tickets", [])
        if t.get("status") in ("ready", "running", "review")
        and (t.get("spec") or "").strip()
    ]
    pipeline.sort(key=_ticket_sort_key)
    tickets = []
    for t in pipeline:
        tickets.append(
            {
                "id": t["id"],
                "title": t.get("title", ""),
                "status": t.get("status"),
                "spec": t.get("spec", ""),
                "branch": t.get("branch", f"ticket/{t['id']}"),
            }
        )
    return {
        "_comment": AUTO_HEADER.strip(),
        "batch_size": 10,
        "concurrency": 3,
        "worktree_base": "../worktrees",
        "git_base": "main",
        "tickets": tickets,
    }


def scan_legacy_ids() -> dict[str, list[str]]:
    root = repo_root()
    hits: dict[str, list[str]] = {}
    scan_roots = [
        root / "docs",
        root / "Design_Docs",
        root / "frontend" / "docs",
        root / "frontend" / "src",
        root / "tickets" / "queue.json",
        root / "CLAUDE.md",
        root / "README.md",
    ]
    for base in scan_roots:
        files = [base] if base.is_file() else [p for p in base.rglob("*") if p.is_file()]
        for f in files:
            rel = str(f.relative_to(root)).replace("\\", "/")
            if any(rel.startswith(p) or p in rel for p in EXEMPT_SCAN_PREFIXES):
                continue
            if rel == "docs/REORG_CHANGELOG.md":
                continue
            try:
                text = f.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue
            matches = STRICT_LEGACY.findall(text)
            if matches:
                hits[rel] = matches
    return hits


def check(strict: bool = False, seed_count: int | None = None) -> list[str]:
    errors = validate_registry()
    registry = load_registry()
    root = repo_root()

    if seed_count is not None and len(registry.get("tickets", [])) != seed_count:
        errors.append(f"Expected {seed_count} registry rows, got {len(registry.get('tickets', []))}")

    expected_rows = 85  # 54 shipped + 31 open
    if len(registry.get("tickets", [])) != expected_rows:
        errors.append(
            f"Registry row count must be {expected_rows} (54 shipped + 31 open), "
            f"got {len(registry.get('tickets', []))}"
        )

    for tid in FORBIDDEN_PHANTOM_IDS:
        if ticket_by_id(registry, tid):
            errors.append(f"Forbidden phantom ticket row: {tid}")

    for row in registry.get("tickets", []):
        spec = (row.get("spec") or "").strip()
        if spec and row.get("status") not in ("idea", "cancelled"):
            if not (root / spec).is_file():
                errors.append(f"{row['id']}: spec missing on disk: {spec}")

    claude = root / "CLAUDE.md"
    roadmap = root / "Design_Docs/Mission_Creator_Architecture/ROADMAP.md"
    for p, start, end in (
        (claude, STATUS_MARKER_START, STATUS_MARKER_END),
        (roadmap, NEXT_MARKER_START, NEXT_MARKER_END),
    ):
        if p.is_file():
            text = p.read_text(encoding="utf-8")
            if start not in text or end not in text:
                errors.append(f"Missing markers in {p.relative_to(root)}")

    try:
        test_gap_analysis_round_trip()
    except RuntimeError as e:
        errors.append(str(e))

    if strict:
        hits = scan_legacy_ids()
        for path, matches in hits.items():
            errors.append(f"Legacy ID in {path}: {len(matches)} match(es)")

        gap = gap_analysis_path()
        if gap.is_file():
            text = gap.read_text(encoding="utf-8")
            if "| priority |" in text or re.search(r"^\d+\.\s+\*\*P[0-3]", text, re.M):
                errors.append("gap_analysis still has priority column or numbered P backlog")

    return errors


def cmd_sync(_: argparse.Namespace) -> None:
    registry = load_registry()
    root = repo_root()
    (root / "docs").mkdir(parents=True, exist_ok=True)

    (root / "docs/TICKET_REGISTRY.md").write_text(
        generate_ticket_registry_md(registry), encoding="utf-8"
    )
    (root / "docs/TICKET_LEAD.md").write_text(
        generate_ticket_lead_md(registry), encoding="utf-8"
    )
    (root / "docs/TICKET_DEV_QUEUE.md").write_text(
        generate_ticket_dev_queue_md(registry), encoding="utf-8"
    )
    (root / "docs/TICKET_BRAINSTORM.md").write_text(
        generate_ticket_brainstorm_md(registry), encoding="utf-8"
    )

    queue = generate_queue_json(registry)
    with (root / "tickets/queue.json").open("w", encoding="utf-8") as f:
        json.dump(queue, f, indent=2)
        f.write("\n")

    claude = root / "CLAUDE.md"
    roadmap = root / "Design_Docs/Mission_Creator_Architecture/ROADMAP.md"
    if claude.is_file() and STATUS_MARKER_START in claude.read_text(encoding="utf-8"):
        inject_status_block(registry)
    if roadmap.is_file() and NEXT_MARKER_START in roadmap.read_text(encoding="utf-8"):
        inject_next_block(registry)

    if gap_analysis_path().is_file():
        sync_gap_analysis_ticket_column(registry)

    print("sync complete")


def cmd_check(args: argparse.Namespace) -> None:
    errors = check(strict=args.strict)
    if errors:
        for e in errors:
            print(f"ERROR: {e}", file=sys.stderr)
        sys.exit(1)
    print("check OK")


def cmd_brief(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    spec = t.get("spec", "")
    tid = t["id"]
    branch = t.get("branch", f"ticket/{tid}")
    print(f"{tid} · {t.get('title', '')}")
    print(f"READ: {spec} (only source of truth)")
    print(f"BRANCH: {branch}")
    print("DO NOT: edit documentation")
    print("VERIFY: cd frontend && npm run build && npm run lint")
    if t.get("acceptance"):
        print("ACCEPTANCE:")
        for a in t["acceptance"]:
            print(f"  - {a}")


def cmd_next(_: argparse.Namespace) -> None:
    registry = load_registry()
    slice_row = next((t for t in registry["tickets"] if t.get("active_slice")), None)
    if slice_row:
        print(f"ACTIVE: {slice_row['id']} slice {slice_row.get('active_slice')}")
    open_t = [
        t
        for t in registry["tickets"]
        if t.get("status") in ("ready", "queued") and t.get("order")
    ]
    open_t.sort(key=_ticket_sort_key)
    for t in open_t[:5]:
        print(f"  {t['id']} — {t.get('title')} ({t.get('status')})")


def cmd_ship(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    t["status"] = "shipped"
    t.pop("active_slice", None)
    save_registry(registry)
    cmd_sync(argparse.Namespace())
    print(f"{args.id} -> shipped")


def cmd_mark_ready(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    if args.spec:
        t["spec"] = args.spec
    spec = (t.get("spec") or "").strip()
    if not spec:
        sys.stderr.write(f"Ticket {args.id} needs a spec path\n")
        sys.exit(1)
    root = repo_root()
    if not (root / spec).is_file():
        sys.stderr.write(f"Spec file not found: {root / spec}\n")
        sys.exit(1)
    for dep in t.get("depends_on") or []:
        dep_row = ticket_by_id(registry, dep)
        if dep_row and dep_row.get("status") not in ("shipped", "cancelled"):
            sys.stderr.write(f"Blocked by {dep} (status={dep_row.get('status')})\n")
            sys.exit(1)
    t["status"] = "ready"
    save_registry(registry)
    cmd_sync(argparse.Namespace())
    print(f"{args.id} -> ready ({spec})")


def cmd_show(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    surfaces = ", ".join(t.get("surfaces") or [])
    impact = ", ".join(t.get("impact") or [])
    print(f"### {t['id']} · {t.get('title', '')}")
    print(f"**Program:** {t.get('program')} · **Where:** {surfaces}")
    if t.get("route"):
        print(f"**Route:** {t['route']}")
    print(f"**Impact:** {impact} · **Status:** {t.get('status')} · **Order:** {t.get('order', '—')}")
    print(f"**Summary:** {t.get('summary', '')}")
    if t.get("depends_on"):
        print(f"**Needs:** {', '.join(t['depends_on'])}")
    if t.get("unblocks"):
        print(f"**Blocks:** {', '.join(t['unblocks'])}")
    if t.get("spec"):
        print(f"**Spec:** `{t['spec']}`")


def cmd_add(args: argparse.Namespace) -> None:
    registry = load_registry()
    tid = f"T-{registry['next_id']:03d}"
    registry["next_id"] += 1
    row: dict[str, Any] = {
        "id": tid,
        "title": args.title,
        "summary": args.summary or args.title,
        "program": args.program,
        "surfaces": args.surfaces.split(","),
        "impact": args.impact.split(","),
        "status": "idea",
    }
    registry["tickets"].append(row)
    save_registry(registry)
    cmd_sync(argparse.Namespace())
    print(f"Added {tid}: {args.title}")


def cmd_remove(args: argparse.Namespace) -> None:
    registry = load_registry()
    before = len(registry["tickets"])
    registry["tickets"] = [t for t in registry["tickets"] if t.get("id") != args.id]
    if len(registry["tickets"]) == before:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    save_registry(registry)
    cmd_sync(argparse.Namespace())
    print(f"Removed {args.id}")


def cmd_reorder(args: argparse.Namespace) -> None:
    registry = load_registry()
    t = ticket_by_id(registry, args.id)
    if not t:
        sys.stderr.write(f"Unknown ticket: {args.id}\n")
        sys.exit(1)
    after_id = args.after.replace("--after", "").strip() if hasattr(args, "after") else None
    anchor = ticket_by_id(registry, args.after)
    if not anchor or anchor.get("order") is None:
        sys.stderr.write(f"Unknown anchor ticket: {args.after}\n")
        sys.exit(1)
    t["order"] = anchor["order"] + 1
    if t.get("status") == "idea":
        t["status"] = "queued"
    save_registry(registry)
    cmd_sync(argparse.Namespace())
    print(f"{args.id} order -> {t['order']} (after {args.after})")


def cmd_plan_batch(_: argparse.Namespace) -> None:
    registry = load_registry()
    queued = [
        t for t in registry["tickets"]
        if t.get("status") in ("queued", "ready") and t.get("order")
    ]
    queued.sort(key=_ticket_sort_key)
    print("Next batch candidates (top 10 by order):")
    for t in queued[:10]:
        spec = t.get("spec") or "(no spec yet)"
        print(f"  {t['id']} — {t.get('title')} [{t.get('status')}] — {spec}")


