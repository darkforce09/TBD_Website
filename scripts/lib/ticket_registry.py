AUTO_HEADER = "<!-- AUTO-GENERATED by ./scripts/ticket sync — DO NOT EDIT -->\n\n"

FORBIDDEN_PHANTOM_IDS = frozenset(
    {
        "T-007",
        "T-014", "T-015", "T-016", "T-017",
        "T-026", "T-027", "T-028",
        "T-041", "T-042", "T-044",
        "T-051",
    }
)

STRICT_LEGACY = re.compile(
    r"\bP[0-3]-[0-9]{2}\b|\bP[0-3]-0[0-9]\b"
    r"|Eden P[0-3]|Eden P0|P0–P2|P0-P3|P0–P3"
    r"|\bP0 registry\b|\bP1 feel\b"
    r"|\bFD-0[0-9]{1,2}\b|\bFD-0xx\b|\bFD backlog\b"
    r"|\bBE-0[0-9]{1,2}\b|\bBE-0xx\b|\bBE backlog\b"
    r"|Track [ABC]\b|Tracks [ABC]/[ABC]/[ABC]|Tracks [ABC]/[ABC]"
    r"|\bA-0[0-9]\b|\bB-0[0-9]\b|\bC-0[0-9]\b"
    r"|\"gap\":\s*\"P[0-3]-"
)

EXEMPT_SCAN_PREFIXES = (
    "artifacts/eden-wiki/",
    "frontend/src/stitch-exports/",
    "Design_Docs/macOS_Blueprints/",
    "Mission_Creator_Mock_Up/",
    ".stitch-backup-exports/",
)

